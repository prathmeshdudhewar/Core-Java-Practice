//All basic data types and prints its output

public class Opd2
{
	public static void main(String arg[])
	{
	  int a=2;
	  char ch='A';
	  float f=3.14f;
	  double d=2.34647;
	  long b=12345;
	  System.out.printf("a=%d/n",a);
	  System.out.printf("ch=%s/n",ch);
	  System.out.printf("float f=%f/n",f);
	  System.out.printf("double d=%f/n",d);
	  System.out.printf("long b=%d/n",b);
	}
}