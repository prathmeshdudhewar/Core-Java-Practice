public class Switch1
{
	public static void main(String x[])
	{
	 int day = 4;
	 switch(day)
	 {
	 case 1:
	 case 2:
            System.out.println("weekday");
	 break;
	 
	 case 3:
	 case 4:
	        System.out.println("midweek");
	 break;
	 
	 default:
	 
	        System.out.println("weekend");
	 }
	}
}
 